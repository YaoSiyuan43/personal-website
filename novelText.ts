她只是伸出手，轻轻按在了发出哀鸣的核心装置上。

那一瞬间，她感觉到了这台古老机器的情绪。它在痛苦，在衰老，在渴望着纯净的能量。

它和她，是同类。

灵汐体内的净魂草本源，与核心装置产生了前所未有的强烈共鸣。

她闭上了眼睛。

下一秒，无法用语言形容的光芒，从她身上爆发出来！

