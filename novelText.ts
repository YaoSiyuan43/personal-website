她从未感知过墨渊如此剧烈的情绪波动。

“园丁……”

灵汐的心里第一次产生了一种名为担忧的情绪。

她想去他身边。

就在这时，房门被猛地撞开。副指挥官李振邦带着一队亲卫冲了进来，他身上还带着城墙上的硝烟味，双眼布满血丝。

“抓住她！”李振邦的声音嘶哑，枪口直指灵汐，“把她带去核心动力室！快！”

一名队长迟疑道：“指挥官……”

“闭嘴！”李振邦吼道，“核心装置撑不了多久了！只有她能替代那个快报废的铁疙瘩！”

他将萧夜散播的流言，当成了最后的救命稻草。

灵汐看着这群充满恐惧的人，皱起了眉。

他们很吵。

他们想用一种她不喜欢的方式，带她去园丁那里。

这种胁迫，让她反感。

“我自己会去。”她轻声说，“你们，让开。”

李振邦因为绝望而失去了理智，只当这是抗拒。“少废话！她不配合就用强的！带走！”

士兵们端着枪，冲了上来。

灵汐的眼神瞬间变得冰冷。她没有动，但一股无可抗拒的力量以她为中心扩散开来。

宁静领域。

冲上来的士兵们动作一僵，脸上的狰狞迅速褪去，取而而代之的是一片平静。他们手中的武器哐当落地，仿佛忘记了自己要做什么。

李振邦惊骇地看着这一幕：“你……你这个怪物！”

