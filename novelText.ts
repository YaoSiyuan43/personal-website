墨渊看着这一切，心中却隐隐有一丝不安。

他查阅了所有关于核心装置的古代文献，发现了一个可怕的记载：核心装置的真正作用，并非放大精神力，而是镇压。

它镇压的，正是基地地底深处那头被称为迷瘴之源的恐怖存在。

迷瘴之源，是这个世界精神污染的根源。所谓的低语迷瘴，不过是它沉睡时无意识散发出的呼吸。萧夜的所作所为，加上灵汐那场惊天动地的能量爆发，已经将它从沉睡中惊醒。

一旦它完全苏醒，喷涌而出的原始迷瘴，将瞬间吞噬整个世界，没有任何静默力场能够抵挡。

唯一的办法，就是让一个新的、更强大的核心去取代旧的核心，彻底镇压它。

而那个新的核心，只能是灵汐。

文献记载，需要净魂草与园丁心意相通，以园丁之躯为锁，以净魂草之力为钥，二者合一，与大地脉络融合，方能成就永恒的宁静圣域。

