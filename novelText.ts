墨渊合上文献，久久不语。

他可以为了人类文明的存续，牺牲自己。但他怎么忍心，让他好不容易才找回来的小家伙，从此被束缚在这片土地上，成为一个没有自由的核心？

他第一次感到了犹豫。

然而，现实没有给他太多选择的时间。

基地外的低语迷瘴浓度开始以几何级数飙升，颜色从灰色变成了不祥的深紫色。更可怕的是，一些前所未见的、更加强大的变异回响者开始出现。它们不再是混乱无序的攻击，而是仿佛被某种意志操控着，一遍又一遍地，对磐石基地发起自杀式的冲击。

它们的目标，是灵汐。是迷瘴之源在呼唤它的食物。

在一次最为猛烈的攻城战中，旧的核心装置发出了不堪重负的哀鸣，布满了裂痕。

墨渊心中轻叹，最终的时刻，还是到了。

他来到灵汐身边，她正在窗边，好奇地看着外面那些疯狂的怪物。

“怕吗？”墨渊从身后轻轻抱住她。

“不怕。”灵汐摇摇头，“它们……好像很想吃我。”

墨渊顿了顿，声音无比艰难，“灵汐，我接下来要做一件事，可能会让你……永远留在这里，和我一起，守护这个家。你愿意吗？”

灵汐转过身，看着墨渊的眼睛。

她还不能完全理解永远的全部含义，但她能看懂墨渊眼中的决绝。

她想起了小雅给她的那个温暖的土豆，想起了基地里那些对着她祈祷的人们，想起了这座城市在她净化后焕发出的勃勃生机。

她的花园，需要她。

最重要的是，她不愿让她的园丁难过。

灵汐踮起脚尖，轻轻吻了一下墨渊的额头，这是她从书上学来的，表示喜欢的动作。

“我愿意。”

墨渊闭上眼，再睁开时，所有的犹豫都化作了坚定的意志。

他牵着灵汐的手，一步步走向核心动力室。这一次，没有人阻拦他们，所有人都自觉地让开道路，对着他们深深鞠躬。

他们来到了已经濒临破碎的旧核心前。

墨渊松开灵汐的手，转身面对着她，一字一句地说道:“记住，我们的精神链接，是击败它的关键。无论你感觉到什么，看到什么，都不要切断它。”

灵汐重重地点了点头。

墨渊深吸一口气，以自身为引，将自己磅礴的精神力毫无保留地注入脚下的大地。

“身即堡垒，镇尔永夜！”

无数刺眼的金色符文从他脚下蔓延开来，组成一个巨大的阵法，将整个地下裂缝牢牢锁住。

巨大的痛苦传来，墨渊闷哼一声，单膝跪地，但他没有倒下。

“灵汐！”他大喊。

灵汐没有丝毫犹豫，她飞身而起，悬浮在旧核心之上，化作了净魂草的本体。

“身即静域，万障皆除！”

她所有的力量，她对这个世界的眷恋，她对墨渊的依恋，全部在这一刻爆发。

她选择了与核心装置彻底融合。

整个磐石基地，连同周围广袤的土地，开始剧烈震动。大地被撕裂，天空被染成紫色。一声来自远古的、充满了贪婪的咆哮，从地底最深处传来。

迷瘴之源苏醒了！

一股纯粹的、足以污染整个世界的原始精神冲击，从裂缝中喷涌而出，直扑灵汐的核心。

一场关乎世界存亡的终极精神对决，在所有人都看不见的层面，展开了。

在无尽的黑暗中，灵汐感觉自己像一叶孤舟，随时都会被倾覆。但那道与墨渊相连的精神链接像缆绳，死死地将她固定在现实世界。

通过这根缆绳，她感受到了墨渊的意志。

“守住！”

她也感受到了磐石基地数百万幸存者的祈愿。

“活下去！”

于是核心装置上空的净魂草光芒更甚。

“我的花园和园丁……”

