“守护……”她喃喃自语，“就是……不让别人欺负你吗？”

“是。”墨渊笑着点头，“也不让别人欺负我们的家。”

“家？”灵汐眨了眨眼，看向窗外。

此刻，整个磐石基地都沐浴在她宁静领域的余晖中。幸存者们走上街头，脸上洋溢着劫后余生的喜悦。

这座冰冷的钢铁城市，第一次，有了一丝家的温度。

灵汐好像有点明白了。

她的花园，不止是墨渊一个人的精神领域。这座城市，这些被她净化的土地，也是她的花园。她需要它们，它们也需要她。

