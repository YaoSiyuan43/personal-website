墨渊冷冷地瞥了他一眼:“从今天起，磐石基地进入最高战备状态，肃清所有回响者的残余势力。后续事宜，等我回来再处理。”

说完，他抱着灵汐，头也不回地离开了。

回到顶层住所，墨渊小心翼翼地将灵汐放在床上。他的心渐渐沉了下去——他能感觉到，灵汐这次消耗极大，精神核心暗淡了许多，短时间极难恢复。

唯一的办法，是比以往任何一次都更为亲密、更为深入的精神交融。不过这种毫无保留的精神敞开，是只有恋人间才能做的事。

墨渊深邃的眼眸中掠过一丝挣扎，随即被更为坚定的温柔所取代。

