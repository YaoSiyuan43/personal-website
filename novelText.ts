但核心装置的警报声已经连成一片，静默力场的外壳上，出现了肉眼可见的裂痕。

墨渊的心，一点点沉了下去。

就在这绝望的时刻，一道白色的身影，悄然出现在动力室门口。

是灵汐。

“园丁。”她看着脸色苍白、嘴角带血的墨渊，声音里带着一丝颤抖。

